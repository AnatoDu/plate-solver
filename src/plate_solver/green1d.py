r"""Функция Грина балки-полосы: шарнир слева, плоскость симметрии справа.

Рассматривается нормированная (длина 1) задача изгиба балки-полосы

.. math::

    D\,w'''' = q, \qquad
    w(0) = w''(0) = 0, \quad w'(1) = w'''(1) = 0.

То есть левый край x=0 шарнирно оперт (w=0, изгибающий момент M∝w''=0),
а правый край x=1 — плоскость симметрии / скользящая заделка
(угол поворота w'=0, перерезывающая сила V∝w'''=0). Такая постановка
естественна для контактной задачи с основанием на [m, L], где правый край
балки является осью симметрии: там пластина ложится на основание плашмя.

Функция Грина (тождественно удовлетворяет всем четырём краевым условиям):

.. math::

    G(x,\xi) = \tfrac{1}{6}(x-\xi)^3 H(x-\xi)
               + \Bigl(\xi - \tfrac{\xi^2}{2}\Bigr)\,x - \tfrac{x^3}{6},

где H — функция Хевисайда. Прогиб от нагрузки q(ξ):

.. math::

    w(x) = \frac{L^4}{D} \int_0^1 G(x,\xi)\,q(\xi)\,d\xi.

Источник: рабочие программы научной школы (Ермоленко и др.), верифицированные
сравнением с точным решением в Maple (см. ``analytic.strip_contact``).
"""

from __future__ import annotations

import numpy as np


def green_strip(x, xi: float) -> np.ndarray:
    r"""G(x, ξ) — функция Грина балки (шарнир слева, симметрия справа).

    Parameters
    ----------
    x  : координата наблюдения (скаляр или массив), 0 ≤ x ≤ 1.
    xi : точка приложения единичной нагрузки, 0 ≤ ξ ≤ 1.
    """
    x = np.asarray(x, float)
    xi = float(xi)
    return (x - xi) ** 3 / 6.0 * np.heaviside(x - xi, 0.5) \
        + (xi - xi ** 2 / 2.0) * x - x ** 3 / 6.0


def green_matrix(n: int) -> np.ndarray:
    r"""Матрица G[i, j] = G(i/n, j/n) для равномерной сетки из n+1 узлов.

    Вычисляется векторно за O(n²) без циклов Python.

    Parameters
    ----------
    n : число разбиений отрезка [0, 1].

    Returns
    -------
    G : массив формы (n+1, n+1).
    """
    t = np.linspace(0.0, 1.0, n + 1)
    x = t[:, np.newaxis]   # (n+1, 1)
    xi = t[np.newaxis, :]  # (1, n+1)
    return (x - xi) ** 3 / 6.0 * np.heaviside(x - xi, 0.5) \
        + (xi - xi ** 2 / 2.0) * x - x ** 3 / 6.0
