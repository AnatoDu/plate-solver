"""config.py — все параметры одного прогона в одном плоском dataclass.

Величины безразмерные (как в ``fix_base2.py``): E, h, q0, a, Delta подобраны так,
чтобы прогиб и реакция были порядка единицы. Жёсткость ``D`` — производная
величина (свойство). Ничего «магического» ниже по стеку: всё берётся отсюда.

Обозначения:
    E, nu, h — упругие постоянные и толщина;
    D        — цилиндрическая жёсткость, D = E·h³ / (12·(1 − ν²));
    q0       — равномерная поперечная нагрузка (q0 > 0 «вниз», w > 0 «вниз»);
    a        — радиус круга (постановка верификации 1D↔2D);
    Delta    — зазор до жёсткого основания (контакт, МОР);
    beta     — шаг метода обобщённой реакции (как в 1D: 0 < β < 2/‖G‖);
    p        — степень Чебышёва по каждой оси (N = (p+1)² базисных функций);
    Q        — число узлов квадратуры Гаусса–Лежандра по каждой оси;
    grid_n   — сетка для реакции/сэмплинга w и визуализации.
"""

from __future__ import annotations

from dataclasses import dataclass


@dataclass
class Config:
    # физика (безразмерно, как в fix_base2.py)
    E: float = 2.1e6
    nu: float = 0.3
    h: float = 1.0
    q0: float = 4.0          # равномерная нагрузка
    # геометрия
    a: float = 1.0           # радиус круга (для верификации)
    # контакт
    Delta: float = 1.0       # зазор до жёсткого основания
    beta: float = 0.01       # шаг МОР (как в 1D)
    max_iter: int = 20000    # итерации МОР
    tol: float = 1e-8        # порог критерия остановки (см. stop)
    stop: str = "dr"         # критерий: "dr" — ‖r_k−r_{k−1}‖ < tol (абсолютный, прежний);
    #                          "comp" — безразмерная KKT-невязка Синьорини < tol
    #                          (см. ContactMOR.solve)
    # дискретизация
    p: int = 12              # степень Чебышёва по каждой оси (N = (p+1)^2)
    Q: int = 64              # число узлов квадратуры по каждой оси
    grid_n: int = 80         # сетка для реакции/сэмплинга w и визуализации

    @property
    def D(self) -> float:
        return self.E * self.h**3 / (12 * (1 - self.nu**2))


__all__ = ["Config"]
