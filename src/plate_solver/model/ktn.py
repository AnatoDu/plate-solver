"""Уточнённая модель изгиба пластины (тип Кармана–Тимошенко–Нагди).

Здесь живут определяющие соотношения и параметры. На старте реализована
только классическая цилиндрическая жёсткость (нужна аналитике-верификатору);
уточнённые операторы (поперечный сдвиг + поперечное обжатие) — заготовки
с описанием того, что предстоит вывести и доказать (положение на защиту № 1).
"""

from __future__ import annotations

from dataclasses import dataclass


def flexural_rigidity(E: float, h: float, nu: float) -> float:
    r"""Классическая цилиндрическая жёсткость пластины.

    .. math:: D = \frac{E\,h^3}{12\,(1-\nu^2)}

    Parameters
    ----------
    E : модуль Юнга.
    h : толщина пластины.
    nu : коэффициент Пуассона (0 <= nu < 0.5).
    """
    if h <= 0:
        raise ValueError("Толщина h должна быть положительной.")
    if not (-1.0 < nu < 0.5):
        raise ValueError("Коэффициент Пуассона nu вне допустимого диапазона.")
    return E * h**3 / (12.0 * (1.0 - nu**2))


@dataclass(frozen=True)
class PlateMaterial:
    """Изотропный упругий материал пластины.

    Attributes
    ----------
    E : модуль Юнга.
    nu : коэффициент Пуассона.
    h : толщина.
    """

    E: float
    nu: float
    h: float

    @property
    def D(self) -> float:
        """Цилиндрическая жёсткость D (классическая)."""
        return flexural_rigidity(self.E, self.h, self.nu)


