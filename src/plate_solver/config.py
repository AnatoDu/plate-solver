"""config.py — все параметры одного прогона в одном плоском dataclass.

Величины безразмерные (как в ``fix_base2.py``): E, h, q0, a, Delta подобраны так,
чтобы прогиб и реакция были порядка единицы. Жёсткость ``D`` — производная
величина (свойство). Ничего «магического» ниже по стеку: всё берётся отсюда.

Обозначения:
    E, nu, h — упругие постоянные и толщина;
    D        — цилиндрическая жёсткость, D = E·h³ / (12·(1 − ν²));
    q0       — равномерная поперечная нагрузка (q0 > 0 «вниз», w > 0 «вниз»);
    a        — радиус круга (постановка верификации 1D↔2D);
    Delta    — зазор до жёсткого основания (контакт, МОР);
    beta     — БЕЗРАЗМЕРНЫЙ коэффициент релаксации МОР: в ContactMOR шаг
               нормируется на усиление оператора, β_eff = β/gain, поэтому
               условие сходимости (теорема 4) имеет вид 0 < β < 2;
    p        — степень Чебышёва по каждой оси (N = (p+1)² базисных функций);
    Q        — число узлов квадратуры Гаусса–Лежандра по каждой оси;
    grid_n   — сетка для реакции/сэмплинга w и визуализации.
"""

from __future__ import annotations

from dataclasses import dataclass


@dataclass
class Config:
    # физика (безразмерно, как в fix_base2.py)
    E: float = 2.1e6
    nu: float = 0.3
    h: float = 1.0
    q0: float = 4.0          # равномерная нагрузка
    # геометрия
    a: float = 1.0           # радиус круга (для верификации)
    # контакт
    Delta: float = 1.0       # зазор до жёсткого основания
    beta: float = 1.2        # релаксация МОР, безразмерная (теорема 4: 0 < β < 2)
    max_iter: int = 20000    # итерации МОР
    tol: float = 1e-8        # порог критерия остановки (см. stop)
    stop: str = "dr"         # критерий: "dr" — ‖r_k−r_{k−1}‖ < tol (абсолютный, прежний);
    #                          "comp" — безразмерная KKT-невязка Синьорини < tol
    #                          (см. ContactMOR.solve)
    # дискретизация
    p: int = 12              # степень Чебышёва по каждой оси (N = (p+1)^2)
    Q: int = 64              # число узлов квадратуры по каждой оси
    grid_n: int = 80         # сетка для реакции/сэмплинга w и визуализации
    # геометрическая нелинейность (теория Кармана, membrane.py) --------------- #
    # Итерация Пикара по замороженным мембранным усилиям N_k (KarmanPlate):
    #   w_{k+1} = (1−θ)·w_k + θ·T(w_k),  θ = karman_relax (недорелаксация);
    #   останов ‖w_{k+1}−w_k‖_{L2} / ‖w_{k+1}‖ < karman_tol.
    # Большие прогибы достигаются НАРАЩИВАНИЕМ нагрузки за n_load_steps шагов
    # (тёплый старт каждого уровня предыдущим решением).
    n_load_steps: int = 1        # число шагов по нагрузке (1 — при малой нагрузке)
    karman_tol: float = 1e-8     # относительный порог останова Пикара по ‖Δw‖
    karman_max_iter: int = 200   # предел итераций Пикара на один уровень нагрузки
    karman_relax: float = 1.0    # недорелаксация θ ∈ (0, 1] (1 — без релаксации)
    karman_method: str = "picard"  # "picard" (по умолч.) | "newton" (ускоритель)
    # полная нелинейная КТН (theory=ktn_full, ktn_full.py) — переиспользует
    # параметры итерации Кармана выше; собственный выбор метода:
    ktn_method: str = "picard"   # "picard" (по умолч.) | "newton" (ускоритель)
    # нелинейный контакт МОР+КТН (contact_nl.py, v0.6.0): схема композиции двух
    # итераций (§4.2) — "merged" (совмещённый, рабочий, быстрый; предмет T7) или
    # "nested" (вложенный, эталон корректности, дорогой).
    contact_scheme: str = "merged"

    @property
    def D(self) -> float:
        return self.E * self.h**3 / (12 * (1 - self.nu**2))


__all__ = ["Config"]
